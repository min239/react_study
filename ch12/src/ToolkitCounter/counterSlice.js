import { createSlice } from '@reduxjs/toolkit'

//reduce대신에 slice생성
//createSlice()는 reducer함수, 초기state등을 알아서 생성해준다.
const counterSlice = createSlice({
   name: 'counterSlice', //slice의 이름(마음래도 지음 연관성있게)
   initialState: { value: 0 }, //sice는 함수를 자동으로 해줌, 초기 state
   reducers: {
      //reducer의 역할(state를 바꿈)을 하는 함수를 지정
      up: (state, action) => {
         //툴킷은 자동으로 불변성 유지를 해주므로 직접state값을 바꿔도 된다.
         //state= {value:0} 0을ㄹ 가지고 옴
         //action.payload = 2
         state.value += action.payload
      },
      down: (state, action) => {
         state.value -= action.payload
      },
   },
})
//리듀서 함수를 내보냄
export default counterSlice.reducer

//액션 생성자 함수를 내보냄
export const {up , down} = counterSlice.actions 

